<script>
import ImageList from "./ImageList";
import Zoom from "./Zoom";
import {mapGetters} from 'vuex'
import {addToCartOrUpdate} from '../../api'

export default {
  name: "Detail",
  components: {
    ImageList,
    Zoom,
  },
  data() {
    return {
      skuNum: 1 // 用户选择购买的商品的数量
    }
  },
  computed: {
    ...mapGetters('detail',["categoryView", "skuInfo"]),
    // 从 skuInfo中拿出商品的属性，用于在下方展示
    skuAttrValueList() {
      return this.skuInfo.skuAttrValueList
    },
    skuSaleAttrValueList() {
      return this.skuInfo.skuSaleAttrValueList
    },
    // 将传给子组件 Zoom 的数据处理一下，保底使其是个空数组，以免传过去的是undefined后续引起报错
    skuImageList() {
      return this.skuInfo.skuImageList || [{}]
    }
  },
  methods: {
    // 检查加入购物车输入框用户的输入是否合法，规定上只能输入：大于 0 的整数
    checkNum() {
      // 先判断用户是不是一上来就按了删除，如果是的话，就先不处理，让输入框为空
      if(this.skuNum !== '') {
        // 用户每按一次键盘就判断，输入的是不是数字，如果不是就把输入的内容删掉
        if(isNaN(this.skuNum)) {
          this.skuNum = parseInt(this.skuNum)
          if(isNaN(this.skuNum)) {
            // 这里处理在输入框为空的情况下用户进行非法输入的情况
            this.skuNum = 1 
          }
        }
        // 这里处理用户输入小数的情况
        this.skuNum = parseInt(this.skuNum)

        // 这里控制购买商品数量最多为999
        if(this.skuNum > 999) {
          alert('最多购买999件')
          this.skuNum = 999
        }
        // 这里控制商品数量最小为1
        if(this.skuNum < 1) {
          this.skuNum = 1
        }
      }
    },
    // 点击加入购物车按钮，将当前商品的信息用 sessionStorage 存储起来
    // 并且，要向服务器通知，将你购买的商品存储在数据库中
    async addToCart() {
      const skuid = this.$route.params.skuid
      const skuNum = this.skuNum
      const response = await addToCartOrUpdate(skuid,skuNum)
      if(response.code == 200) {
        // code为200表示添加购物车成功
        // 若成功，则跳转到添加购物车成功的路由，并展示添加成功的商品
        sessionStorage.setItem('skuInfo',JSON.stringify(this.skuInfo))
        this.$router.push({name:'addcartsuccess',params:{skuid,skuNum}})
      }else{
        // code若不为200则代表加入购物车失败，此时提醒一下用户
        alert('加入购物车失败！')
      }
    }
  },
  watch: {
    // 这里监视一下skuNum以应对 + - 按钮的触发
    skuNum: {
      handler(newValue) {
        if(newValue>999) {
          alert('最多购买999件')
          this.skuNum = 999
        }
        if(newValue<1) {
          this.skuNum = 1
        }
      }
    }
  },
  mounted() {
    // 组件挂载完毕就通知vuex仓库向服务器发请求获取商品详情的数据
    this.$store.dispatch('detail/goodInfo',this.$route.params.skuid)
    
  },
};
</script>

